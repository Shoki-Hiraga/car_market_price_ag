<p>
    25年ルールとは、製造から25年以上経過した右ハンドルのクルマをアメリカ国内にそのまま輸入できる法律の例外にあたる特別ルールのことです。<br>
    アメリカでは、原則として右ハンドル車を輸入できません。<br>
    つまり、日本車やイギリス車などをそのまま輸入することができないのです。
    <br>
    しかし、製造から25年が経過したクルマであれば、クラシックカーとして登録することが可能となるため、右ハンドル車である日本車やイギリス車をそのまま輸入できるようになります。<br>
    また、関税や排ガス規制も対象外になることも25年ルールの特徴です。<br>
    このようなアメリカのクラシックカー登録制度（法律の例外）が25年ルールとなります。
</p>
<p class="ex_strong">つまり、25年経過した日本車は海外需要が高い場合、<strong>高値で売れる</strong>傾向があり、新車から25年に近づくにつれ相場が大きく変動することがあります。</p>
